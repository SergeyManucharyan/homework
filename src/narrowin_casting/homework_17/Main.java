package narrowin_casting.homework_17;

public class Main {
    public static void main(String[] args) {
//        17. Ստեղծել int ff = 9999 և byte gg փոփոխականներ, կատարել Cast գործողություն  ff -> gg  և տպել console -ում․
        int ff = 9999;
        byte gg = (byte) ff;
        System.out.println(ff + " " + gg);
    }
}
