package narrowin_casting.homework_16;

public class Main {
    public static void main(String[] args) {
//        16. Ստեղծել double dd = 9.78d և int ii փոփոխականներ, կատարել Cast գործողություն  dd -> ii  և տպել console -ում․
        double dd = 9.78d;
        int ii = (int) dd;
        System.out.println(dd + " " + ii);
    }
}
